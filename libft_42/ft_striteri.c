#include <stdlib.h>
#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;

	i = 0;
	if (!s || !f)
		return ((void) 0);
	while (i < ft_strlen(s))
	{
		f(i, &s[i]);
		i++;
	}
}
