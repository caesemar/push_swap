#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*t;
	t_list	*tmp;

	t = *lst;
	while (t)
	{
		tmp = t->next;
		(del)(t->content);
		free (t);
		t = tmp;
	}
	*lst = NULL;
}
